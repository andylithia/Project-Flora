int main(void)
{

  /* USER CODE BEGIN 1 */
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM2_Init();
  MX_TIM1_Init();
  MX_I2C1_Init();
  MX_TIM14_Init();
  MX_TIM16_Init();
  MX_TIM17_Init();
  MX_SPI1_Init();
  MX_IWDG_Init();

  /* USER CODE BEGIN 2 */
  // EE_Init();
  // Reset Peripherals
  HAL_GPIO_WritePin(IV9LATCH_GPIO_Port, IV9LATCH_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(PNRST_GPIO_Port, PNRST_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(PNRST_GPIO_Port, PNRST_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(PNRST_GPIO_Port, PNRST_Pin, GPIO_PIN_SET);

  // Check status by verifying YEAR data from DS3231
  // If YEAR = 00, the DS3231 is hard reseted, which indicates a fresh power on
  DS3231_GetTime(&hi2c1, &g_time_reg, 0xFF);
  g_freshPON = g_time_reg.YEAR==0;

  if(g_freshPON){
	  DS3231_control_t DS3231_initInst;
	  DS3231_initInst.CTRL1 = 0;
	  DS3231_initInst.CTRL2_STAT = 0;
	  DS3231_Config(&hi2c1, &DS3231_initInst, 0xFF);

	  g_time_reg.YEAR=0x18;
	  DS3231_SetTime(&hi2c1, &g_time_reg,0xFF);
  }

  // Init with maximum brightness
	g_curScheme = 0;
	for(int i=0; i<4; i++)
		g_scheme_reg[g_curScheme].IV9_pValue[i] = IV9_PULSE_MAX;
	g_scheme_reg[g_curScheme].LED_pValue.R = RGB_PULSE_MAX;
	g_scheme_reg[g_curScheme].LED_pValue.G = RGB_PULSE_MAX/4;
	g_scheme_reg[g_curScheme].LED_pValue.B = 0;
	// The buzzer is disabled for this version
	// HAL_TIM_PWM_Start(&htim14, TIM_CHANNEL_1);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);

  g_currentMenu = &menu_l0_intro;
  MFA_CALL(g_currentMenu, EVENT_ENTER, 0);

  // Start the core timer
  HAL_TIM_Base_Start_IT(&htim16);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1) {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  	MFA_CALL(g_currentMenu, EVENT_LOOP, 0);			// Execute Loop Code
	  	HAL_IWDG_Refresh(&hiwdg);						// Feed the dog
	  	if(g_sysCfg&CONF_LPM_ENABLE){
			HAL_SuspendTick();
			HAL_PWR_EnterSLEEPMode(0, PWR_SLEEPENTRY_WFI);
			// Wait until another timer interrupt wakes it up
			__NOP();
			HAL_ResumeTick();
	  	}
  }
  /* USER CODE END 3 */

}

